// TRIM
// Elimina los espacios en blanco al principio y al final de una cadena.

const stringWithSpaces = "    ¡Hola mundo!    ";
const trimmedString = stringWithSpaces.trim();

console.log('Cadena original:', stringWithSpaces); // Cadena original:     ¡Hola mundo! 
console.log('Cadena trimmada:', trimmedString); // Cadena trimmada: ¡Hola mundo!